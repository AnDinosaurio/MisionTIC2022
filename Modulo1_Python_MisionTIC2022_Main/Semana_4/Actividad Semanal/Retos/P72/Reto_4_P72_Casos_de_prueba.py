def RegistroDiario(produccion):
    #Importar la funcion "reduce" desde la libreria "functools"
    from functools import reduce

    #Declarar constante "inrcementoProduccion"
    inrcementoProduccion = 30000000

    #Primer lambda que suma el total de cada tupla de cada lista
    #Lambda de "y", recorre todas las tuplas(elemento 1 y 2) de produccionDiaria(registro) y multiplica cantidad*Monto bruto
    #Lambda de "x", recorre cada primer elemento de cada lista y guarda el # de orden
    #(+) suma elementos en la lista resultante
    producionDiaria = list(map(lambda x: [x[0]] + list(map(lambda y: y[1]*y[2], x[1:])), produccion))
    #print(ordenTotal)

    #Segundo lambda suma los totales de todas las listas
    #Lambda de "a,b", suma cada valor de las listas resultantes y lo reduce a un valor por lista
    #Lambda de "x", recorre cada primer elemento de cada lista y guarda el # de orden
    #(+) suma elementos en la lista resultante
    producionDiaria = list(map(lambda x: [x[0]] + [reduce(lambda a,b: round(a + b,2), x[1:])], producionDiaria)) 
    #print(ordenTotal)

    #Tercer lambda suma el incremento de 50.000 pesos
    #La primera "x", recorre cada registro(lista)
    #La segunda "x", recorre cada total evaluando la condicion
    #La tercera "x", suma 50.000 pesos si el monto diario asciende a 30.000.000 de pesos
    #Si x[1] de cada lista es menor a ordenMinimaDiaria(30.000.000) suma 50.000 pesos
    # por  concepto  de  embalajes
    producionDiaria = list(map(lambda x: x if x[1] >= inrcementoProduccion else (x[0], x[1] + 50000), producionDiaria))

    print('------------------------ Inicio Registro diario ---------------------------------')
    #Ciclo para imprimir el total de cada compra
    for x in range(len(producionDiaria)):
        print(f'El registro diario n√∫mero {producionDiaria[x][0]} tiene un monto total en pesos de {producionDiaria[x][1]:,.2f}')
    print('-------------------------- Fin Registro diario ----------------------------------')

